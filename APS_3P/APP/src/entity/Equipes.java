package entity;

public class Equipes {
   private String equipe;
   private int voltas;
   private int tempo;
   
   public String getEquipe() {
       return equipe;
   }
   public void setEquipe(String equipe) {
       this.equipe = equipe;
   }
   public int getVoltas() {
       return voltas;
   }
   public void setVoltas(int voltas) {
       this.voltas = voltas;
   }
   public int getTempo() {
       return tempo;
   }
   public void setTempo(int tempo) {
       this.tempo = tempo;
   }
}
